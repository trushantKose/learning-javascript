// else if for vote system
let age = 14;

if(age >= 18){
    console.log("you can vote");
} else if(age < 18){
    console.log("you cannot vote");
}

// else if for grade system
let marks = 75;

if(marks >= 80){
    console.log("A+");
} else if(marks >= 60){
    console.log("B+");
} else if(marks >= 33){
    console.log("C+");
} else if(marks < 33){
    console.log("Fail");
}

// else if for month of season's
let month = "january";

if(month == "january"){
    console.log("winter season");
} else if(month == "april"){
    console.log("summer season");
} else if(month == "july"){
    console.log("rainy season");
}