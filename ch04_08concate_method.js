// concate() method : joins multiple arrays & returns result
let marvel_heroes = ['thor', 'iron-man', 'captain-america', 'spider-man'];
let dc_heroes = ['batman', 'superman', 'wonder women'];
console.log(marvel_heroes.concat(dc_heroes));

let india_heroes = ['shakti-man', 'krish'];
console.log(marvel_heroes.concat(dc_heroes).concat(india_heroes));