// Arithmetic Operators (+,-,*,/,%)
let a = 10;
let b = 20;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

// Unary Operators (++,--)
let x = 10;
//post-increment and decrement
console.log(x++);   //10
console.log(++x);   //12

// Assignment Operator(=, +=, -=, *=, /=, %=)
// comparison operator(>, <, <= ,>= ,== , !=) :-compare betn two values
let age = 17;
console.log(age > 18); //false
console.log(age < 18); //true
console.log(age >= 18); //false
console.log(age <= 18); //true
console.log(age == 18); //false
console.log(age != 18); //true

