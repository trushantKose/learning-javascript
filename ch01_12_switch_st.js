// Switch statement: - Used when we have some fixed values that we need to compare to.
let color = "red";
switch(color){
    case "red":
        console.log("stop");
        break;
    case "yellow":
        console.log("slow down");
        break;
    case "green":
        console.log("Go");
        break;
    default:
        console.log("broken light");
}